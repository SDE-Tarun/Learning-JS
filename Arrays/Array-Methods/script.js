var arr = [1,2,3,4,5];

// PUSH - ADD ELEMENT AT THE LAST OF ARRAY
arr.push(6);
console.log(arr);

// POP - REMOVE ELEMENT FROM THE LAST OF ARRAY
arr.pop();
console.log(arr);

// SHIFT - REMOVE ELEMENT FROM THE BEGINNING OF ARRAY
arr.shift();
console.log(arr);

// UNSHIFT - ADD ELEMENT AT THE BEGINNING OF ARRAY
arr.unshift(1);
console.log(arr);

// SPILCE - RETURNS THE REMOVED ITEMS
// (START INDEX, DELETION COUNT, ELEMENTS TO BE INSERTED)

arr.splice(1,2,45,55);  
console.log(arr);



















